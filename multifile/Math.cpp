#include "Math.h"
int Math::add(int num1, int num2) {
	return Math::result = num1 + num2;
}
int Math::subtract(int num1, int num2) {
	return Math::result = num1 - num2;
}
int Math::multiply(int num1, int num2) {
	return Math::result = num1 * num2;
}
int Math::divide(int num1, int num2) {
	return Math::result = num1 / num2;
}